    public User register(SignUpDto request) {

        boolean isValidEmail = emailValidator.test(request.getEmail());

        if (!isValidEmail){
            throw new IllegalStateException("Email Invalid");
        }

        User user = new User(
                request.getFullName(),
                request.getEmail(),
                request.getPassword(),
                UserRole.USER
        );

        this.signUpUser(
                user
        );

        String link = "http://localhost:8080/api/auth/confirmEmail?userid="+user.getId();

        emailSender.send(request.getEmail(), buildEmail(request.getFullName(),link));

        return user;

    }

    public void signUpUser(User user){

        boolean userEmailExists = userRepository.findByEmail(user.getEmail()).isPresent();

        if (userEmailExists) {
            throw new IllegalStateException("Email already exists");
        }

        String encodedPassword = passwordEncoder.encode(user.getPassword());

        user.setPassword(encodedPassword);

        userRepository.save(user);

    }

    @SneakyThrows
    public int enableUserEmail(int userid) {
        Optional<User> userOptional = userRepository.findById(userid);

        if (!userOptional.isPresent()) {
            throw new Exception("User with ID " + userid + " not found.");
        }

        User user = userOptional.get();

        if (user.isEnabled() && user.getEmail_verified_at() != null) {
            throw new Exception("Email for user with ID " + userid + " is already verified.");
        }

        return userRepository.enableUserEmail(userid);
    }


    @SneakyThrows
    public User authenticate(LoginDto input) {
        String user = input.getEmail();
        User existingUser = userRepository.findByEmail(user).get();
        if (!existingUser.isEnabled()){
            throw new Exception("Email not Verified");
        }
        authenticationManager.authenticate(
                new UsernamePasswordAuthenticationToken(
                        input.getEmail(),
                        input.getPassword()
                )
        );

        return userRepository.findByEmail(input.getEmail()).orElseThrow();
    }

